
# Suma
print(2 + 2)

# Resta
print(4 - 2)

# Multiplicación
print(4 * 2)

# División
print(25 / 4)

# División sin decimal
print(25 // 4)

# Otras operaciones
# Potencia
print(2 ** 3)

# Módulo: Lo que sobra de la división o Residuo
print(6 % 4)

# Redondear
print(round(3.1))  # 3
print(round(3.5))  # 4

# 2, No redondea a 3 porque el .5 redondea al próximo numero par seigue el 3 que es impar
print(round(2.5))

print(round(2.51))  # De esta forma si redondea a 3 porque ya es mayor .51 a .5

# Valor absoluto
print(abs(-20))
